import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const allowlist = JSON.parse(readFileSync(join(root, 'app/data/allowlist.json'), 'utf8'));

const foods = allowlist
  .map((e) => ({
    name: e.name,
    category: e.category,
    fdcId: e.fdcId,
    basis: e.basis,
    caloriesPer100g: e.fallback.caloriesPer100g,
    proteinGPer100g: e.fallback.proteinGPer100g,
    fatGPer100g: e.fallback.fatGPer100g,
    calciumMgPer100g: e.fallback.calciumMgPer100g,
    phosphorusMgPer100g: e.fallback.phosphorusMgPer100g,
    source: `Allowlist fallback (FDC ${e.fdcId}, ${e.basis})`,
  }))
  .sort((a, b) => a.name.localeCompare(b.name));

const lines = [
  '// AUTO-GENERATED by scripts/sync-fdc.mjs — do not edit by hand.',
  '// Generated from allowlist fallbacks (run npm run sync:fdc with FDC_API_KEY to refresh from USDA).',
  `// Generated: ${new Date().toISOString()}`,
  "import type { FoodIngredient } from './ingredientTypes';",
  '',
  'export const FOODS: FoodIngredient[] = [',
];

for (const food of foods) {
  lines.push('  {');
  for (const [key, value] of Object.entries(food)) {
    lines.push(`    ${key}: ${JSON.stringify(value)},`);
  }
  lines.push('  },');
}
lines.push('];');
lines.push('');

const outPath = join(root, 'app/data/foods.generated.ts');
writeFileSync(outPath, lines.join('\n'));
console.log(`Wrote ${foods.length} foods to ${outPath}`);
